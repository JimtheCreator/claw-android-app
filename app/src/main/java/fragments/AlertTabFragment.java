package fragments;

import android.content.Context;
import android.os.Bundle;

import androidx.annotation.NonNull;
import androidx.annotation.Nullable;
import androidx.fragment.app.Fragment;

import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;

import com.claw.ai.R;
import com.claw.ai.databinding.FragmentAlertTabBinding;

import bottomsheets.NewAlertBottomSheetFragment;

public class AlertTabFragment extends Fragment {

    FragmentAlertTabBinding binding;

    @Override
    public void onCreate(@Nullable Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
    }

    @Override
    public View onCreateView(@NonNull LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        binding = FragmentAlertTabBinding.inflate(inflater, container, false);
        return binding.getRoot();
    }

    @Override
    public void onViewCreated(@NonNull View view, @Nullable Bundle savedInstanceState) {
        super.onViewCreated(view, savedInstanceState);

        binding.createNewAlert.setOnClickListener(v -> {
            NewAlertBottomSheetFragment newAlertFragment = NewAlertBottomSheetFragment.newInstance();
            newAlertFragment.show(getParentFragmentManager(), "NewAlertBottomSheetFragment");
        });
    }

    @Override
    public void onAttach(@NonNull Context context) {
        super.onAttach(context);
    }

    @Override
    public void onDetach() {
        super.onDetach();
    }
}